public class InOrderBean {
    private String id;
    private long date;
    private String status;
    private String areaNo;
    private String plateNo;
    private String depotNo;
    private String outOrderNo;
    private String inOrderNo;
    private String process;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public long getDate() {
        return date;
    }

    public void setDate(long date) {
        this.date = date;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getAreaNo() {
        return areaNo;
    }

    public void setAreaNo(String areaNo) {
        this.areaNo = areaNo;
    }

    public String getPlateNo() {
        return plateNo;
    }

    public void setPlateNo(String plateNo) {
        this.plateNo = plateNo;
    }

    public String getDepotNo() {
        return depotNo;
    }

    public void setDepotNo(String depotNo) {
        this.depotNo = depotNo;
    }

    public String getOutOrderNo() {
        return outOrderNo;
    }

    public void setOutOrderNo(String outOrderNo) {
        this.outOrderNo = outOrderNo;
    }

    public String getInOrderNo() {
        return inOrderNo;
    }

    public void setInOrderNo(String inOrderNo) {
        this.inOrderNo = inOrderNo;
    }

    public String getProcess() {
        return process;
    }

    public void setProcess(String process) {
        this.process = process;
    }
}
